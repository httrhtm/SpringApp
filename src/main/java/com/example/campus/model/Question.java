package com.example.campus.model;


//entityクラス
public class Question {

	private int id;
	private String question;

	//getter,setterの設定
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
}
